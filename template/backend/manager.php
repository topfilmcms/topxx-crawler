<h1>XPhim-xAPI</h1>
<p><strong>Topxx.vip</strong> là kho dữ liệu phim tập trung, cung cấp đầy đủ meta (tiêu đề, mô tả, poster, diễn viên, thể loại, quốc gia, nguồn phát) cho các nền tảng xem phim thông qua API đơn giản và linh hoạt.</p>
<p>Plugin <strong>XPhim-xAPI</strong> phiên bản 1.0.1 giúp bạn quản lý phim trên WordPress và đồng bộ dữ liệu từ Topxx.vip.</p>
<hr>
<h2>Hướng dẫn sử dụng</h2>
<h3>Lỗi 404</h3>
<p>Vào Cài đặt -> <a href="/wp-admin/options-permalink.php">Đường dẫn tĩnh</a> -> Lưu</p>
<h3>Thêm Menu</h3>
<p>Vào Giao diện -> <a href="/wp-admin/nav-menus.php">Menu</a> -> Tạo Primary Menu</p>
<p>Trên cùng bên phải -> Tùy chọn hiển thị -> Tích chọn danh mục của XPhim</p>
<h3>Thêm widget</h3>
<p>Vào Giao diện -> <a href="/wp-admin/widgets.php">Widget</a> -> Kéo widget vào phần muốn hiển thị</p>
<h3>Sửa logo</h3>
<p>Vào Giao diện -> <a href="/wp-admin/customize.php">Tùy biến</a> -> Nhận dạng site</p>
<h3>Số bài hiển thị trong trang</h3>
<p>Vào Cài đặt -> <a href="/wp-admin/options-reading.php">Đọc</a> -> Hiển thị nhiều nhất</p>
